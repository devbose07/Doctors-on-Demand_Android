package com.globussoft.readydoctors.patient.model;

public class PsychologistsModel
{
	public String psychologistName;
	public String psychologistQualification;
	public String psychologistBiography;
	public String psychologistPic;
	public String getPsychologistName() 
	{
		return psychologistName;
	}
	public void setPsychologistName(String psychologistName) 
	{
		this.psychologistName = psychologistName;
	}
	public String getPsychologistQualification() 
	{
		return psychologistQualification;
	}
	public void setPsychologistQualification(String psychologistQualification) 
	{
		this.psychologistQualification = psychologistQualification;
	}
	public String getPsychologistBiography() 
	{
		return psychologistBiography;
	}
	public void setPsychologistBiography(String psychologistBiography) 
	{
		this.psychologistBiography = psychologistBiography;
	}
	public String getPsychologistPic() 
	{
		return psychologistPic;
	}
	public void setPsychologistPic(String psychologistPic) 
	{
		this.psychologistPic = psychologistPic;
	}
	

}
